public class Test {
    public static void main(String[] args){
        // While-Loop
        int i = 0;
        int a = 5;
        while (i < a){
            System.out.println(i);
            i++;
        }

        //Do-While-Loop
        i = 0;
        do {
            System.out.println(i);
            i++;
        } while (i < a);

        //For-Loop

        for (int x = 0; x < a; x++){
            System.out.println(x);
        }

    }
}
